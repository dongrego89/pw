def suma(num1,num2):
    "Funcion que suma num1 a num2"
    return num1+num2
def resta(num1,num2):
    "Funcion que resta a num1 num2"
    return num1-num2
def multiplicacion(num1,num2):
    "Funcion que multiplica num1 por num2"
    return num1*num2
def potencia(num1,num2):
    "Funcion que eleva num1 al exponente num2"
    return num1 ** num2
